function getSummonerSpell(spellId) {
    switch(spellId) {
        case 1:
            return "Boost";
        case 12:
            return "Teleport";
        case 30:
            return "PoroRecall";
        case 14:
            return "Dot";
        case 6:
            return "Haste";
        case 32:
            return "Snowball";
        case 7:
            return "Heal";
        case 11:
            return "Smite";
        case 3:
            return "Exhaust";
        case 31:
            return "PoroThrow";
        case 13:
            return "Mana";
        case 2:
            return "Clairvoyance";
        case 21:
            return "Barrier";
        case 4:
            return "Flash";
        case 17:
            return "Garrison";
    }
}